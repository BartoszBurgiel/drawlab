package canvas

/*
	ASCII CODES FOR COLORS
*/

// RED color of the printed text
const RED string = "\033[0;31m"

// GREEN color of the printed text
const GREEN string = "\033[0;32m"

// BLUE color of the printed text
const BLUE string = "\033[0;34m"

// PURPLE color of the printed text
const PURPLE string = "\033[0;35m"

// CYAN color of the printed text
const CYAN string = "\033[0;36m"

// YELLOW color of the printed text
const YELLOW string = "\033[1;33m"

// WHITE color of the printed text
const WHITE string = "\033[1;37m"

// CLEAR the color of the printed text
const CLEAR string = "\033[1m\033[0m"
